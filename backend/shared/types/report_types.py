class ReportSection(BaseModel):
    """A single ordered section within the final assembled report."""

    section_order: int = Field(
        ...,
        ge=1,
        description="Position in the report (1-indexed). Canonical order from SECTION_ORDER.",
    )
    section_key: SectionKey = Field(
        ..., description="Section identifier. Unique within a FinalReport."
    )
    section_title: str = Field(..., description="Display title for this section.")
    content_markdown: str = Field(
        ...,
        description="Markdown content for this section only.",
    )


class ReportMetadata(BaseModel):
    """Structured summary of key signals extracted from report content.

    Enables lightweight querying of report signals without parsing markdown.
    Persisted alongside the full markdown in Supabase.
    """

    primary_language: str = Field(
        ..., description="Primary programming language detected in the repository."
    )
    detected_architecture_pattern: ArchitecturePattern = Field(
        ..., description="Architecture pattern classified by the Architecture Agent."
    )
    architecture_confidence: Confidence = Field(
        ..., description="Confidence level of the architecture pattern classification."
    )
    security_finding_count: int = Field(
        ..., ge=0, description="Total number of security findings in the report."
    )
    finding_counts_by_severity: dict[str, int] = Field(
        ...,
        description="Security finding count per severity level, keyed by Severity enum name.",
    )
    highest_severity_finding: Severity | None = Field(
        default=None,
        description="Highest severity level present in security findings. None when no findings.",
    )
    recommendation_count: int = Field(
        ..., ge=0, description="Total number of recommendations in the report."
    )
    p1_recommendation_count: int = Field(
        ..., ge=0, description="Count of P1 (immediate action) recommendations."
    )
    rag_chunks_used_count: int = Field(
        ..., ge=0, description="Total RAG chunks incorporated across all recommendations."
    )
    analysis_duration_seconds: int = Field(
        ..., ge=1, description="Total elapsed time from job start to report completion in seconds."
    )
    total_files_analyzed: int = Field(
        ..., ge=0, description="Total files successfully parsed during the analysis."
    )
    total_llm_tokens_used: int = Field(
        ..., ge=0, description="Aggregate token count (input + output) across all LLM calls."
    )

    @field_serializer("highest_severity_finding")
    def serialize_severity(self, value: "Severity | None") -> str | None:
        return value.name if value is not None else None


class FinalReport(BaseModel):
    """The complete assembled analysis report persisted to Supabase.

    Produced by the Report Assembly Service and written to Supabase by the
    Persistence Node. This is the terminal artifact of the entire workflow.

    Immutability rule:
    - FinalReport is immutable after persistence.
    - Re-analysis creates a new FinalReport with a new report_id.
    - The API Gateway reads FinalReport but never modifies it.

    Section ordering:
    - sections must contain exactly 6 entries covering all SectionKey values.
    - section_order values must be 1–6 matching SECTION_ORDER from config/constants.py.
    """

    report_id: UUID = Field(..., description="Unique identifier for this report.")
    job_id: UUID = Field(..., description="Parent analysis job. Foreign key to JobRecord.")
    repo_url: str = Field(..., description="Repository URL that was analysed.")
    repo_name: str = Field(..., description="Repository name extracted from the URL.")
    generated_at: datetime = Field(
        ..., description="UTC timestamp when report assembly completed."
    )
    schema_version: str = Field(
        default=REPORT_SCHEMA_VERSION,
        description=(
            f"Contract schema version. MVP value: '{REPORT_SCHEMA_VERSION}'. "
            "Incremented on breaking changes."
        ),
    )
    markdown_content: str = Field(
        ...,
        min_length=1,
        description="Complete assembled report as a single markdown string.",
    )
    metadata: ReportMetadata = Field(
        ...,
        description="Structured key signals for lightweight querying without parsing markdown.",
    )
    sections: list[ReportSection] = Field(
        ...,
        description=(
            "Ordered section records. Must contain exactly 6 entries covering "
            "all SectionKey values in canonical order."
        ),
    )

    @field_validator("schema_version", mode="before")
    @classmethod
    def validate_schema_version(cls, value: str) -> str:
        """Enforce that schema_version matches the current contract version."""
        if str(value) != REPORT_SCHEMA_VERSION:
            raise ValueError(
                f"schema_version must be '{REPORT_SCHEMA_VERSION}'. Got '{value}'."
            )
        return value

    @model_validator(mode="after")
    def validate_sections_complete_and_ordered(self) -> "FinalReport":
        """Enforce exactly 6 sections covering all SectionKey values in canonical order."""
        expected_keys = [SectionKey(key) for key in SECTION_ORDER]

        if len(self.sections) != len(expected_keys):
            raise ValueError(
                f"FinalReport must contain exactly {len(expected_keys)} sections. "
                f"Got {len(self.sections)}."
            )

        for i, (section, expected_key) in enumerate(zip(self.sections, expected_keys), start=1):
            if section.section_order != i:
                raise ValueError(
                    f"Section at position {i} has section_order={section.section_order}. "
                    f"Expected {i}."
                )
            if section.section_key != expected_key:
                raise ValueError(
                    f"Section at position {i} has section_key='{section.section_key.value}'. "
                    f"Expected '{expected_key.value}'."
                )
        return self

    @model_validator(mode="after")
    def validate_p1_count_le_total(self) -> "FinalReport":
        """Enforce that p1_recommendation_count does not exceed recommendation_count."""
        meta = self.metadata
        if meta.p1_recommendation_count > meta.recommendation_count:
            raise ValueError(
                f"p1_recommendation_count ({meta.p1_recommendation_count}) cannot exceed "
                f"recommendation_count ({meta.recommendation_count})."
            )
        return self
