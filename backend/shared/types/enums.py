"""Application-wide enumeration definitions.

All enums used across the ArchMind AI codebase are defined here and imported
from this single module. No other module defines its own copy of these values.

Dependency rule: this module imports only from Python stdlib.
"""

from __future__ import annotations

from enum import Enum, IntEnum


# ---------------------------------------------------------------------------
# Analysis quality enumerations
# ---------------------------------------------------------------------------


class Severity(IntEnum):
    """Severity levels for security findings and architecture weaknesses.

    Defined as IntEnum to support natural comparisons:
        Severity.CRITICAL > Severity.HIGH  # True
        max(findings, key=lambda f: f.severity)  # returns highest severity

    Assignment rule: severity is always assigned by the rule engine,
    never by LLM output.
    """

    CRITICAL = 5
    HIGH = 4
    MEDIUM = 3
    LOW = 2
    INFO = 1


class Priority(str, Enum):
    """Recommendation priority levels.

    Derivation rule:
    - P1: source finding has CRITICAL or HIGH severity.
    - P2: source finding has MEDIUM severity or significant architectural violation.
    - P3: LOW severity findings, quality improvements, non-blocking concerns.
    """

    P1 = "P1"
    P2 = "P2"
    P3 = "P3"


class Confidence(str, Enum):
    """Confidence levels for pattern detection and agent assessments.

    Signal basis:
    - HIGH:   Three or more independent corroborating signals.
    - MEDIUM: Two signals, or one strong signal.
    - LOW:    Single weak or ambiguous signal.

    LOW confidence findings must use qualified language in LLM narrative.
    """

    HIGH = "HIGH"
    MEDIUM = "MEDIUM"
    LOW = "LOW"


class EffortEstimate(str, Enum):
    """Rough effort estimate for implementing a recommendation.

    Generated by LLM with an explicit uncertainty qualifier in the prompt.
    Treat as directional guidance, not a precise estimate.
    """

    LOW = "LOW"
    MEDIUM = "MEDIUM"
    HIGH = "HIGH"


# ---------------------------------------------------------------------------
# Architecture analysis enumerations
# ---------------------------------------------------------------------------


class ArchitecturePattern(str, Enum):
    """Detected software architecture patterns.

    Classification is produced by the Architecture Agent rule engine
    from signals in the ParsedCodeRepresentation.
    """

    LAYERED = "LAYERED"
    MVC = "MVC"
    HEXAGONAL = "HEXAGONAL"
    SERVICE_BASED = "SERVICE_BASED"
    MONOLITH_UNDIFFERENTIATED = "MONOLITH_UNDIFFERENTIATED"
    UNKNOWN = "UNKNOWN"


class PatternStrength(str, Enum):
    """Strength of evidence supporting a detected architecture pattern indicator."""

    STRONG = "STRONG"
    MODERATE = "MODERATE"
    WEAK = "WEAK"


class CouplingType(str, Enum):
    """Classification of coupling problem type detected in a source file."""

    HIGH_FAN_IN = "HIGH_FAN_IN"
    """More modules import this file than the fan-in threshold allows."""

    HIGH_FAN_OUT = "HIGH_FAN_OUT"
    """This file imports more modules than the fan-out threshold allows."""

    BOTH = "BOTH"
    """Exceeds both fan-in and fan-out thresholds simultaneously."""


class SignalLevel(str, Enum):
    """Generic HIGH / MEDIUM / LOW level for cohesion and coupling assessments.

    Used for both CohesionAssessment (HIGH = good) and CouplingLevel
    (HIGH = bad). The semantic direction is determined by the field name
    and context, not by this enum itself.
    """

    HIGH = "HIGH"
    MEDIUM = "MEDIUM"
    LOW = "LOW"


class DefinitionKind(str, Enum):
    """Type of code definition extracted from a Tree-sitter AST node."""

    FUNCTION = "function"
    CLASS = "class"
    METHOD = "method"


