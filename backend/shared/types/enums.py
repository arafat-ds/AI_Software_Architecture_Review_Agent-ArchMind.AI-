class JobStatus(str, Enum):
    """Persisted job lifecycle states stored in Supabase.

    Transitions:
        PENDING → RUNNING → COMPLETE
        PENDING → RUNNING → FAILED
        PENDING → FAILED  (validation failure before workflow start)

    COMPLETE and FAILED are terminal states. No further transitions allowed.
    """

    PENDING = "PENDING"
    RUNNING = "RUNNING"
    COMPLETE = "COMPLETE"
    FAILED = "FAILED"


class WorkflowStatus(str, Enum):
    """In-memory LangGraph workflow execution states stored in AnalysisState.

    Transitions are strictly forward. No backward transitions are permitted.
    Fatal errors at any state transition to FAILED.
    """

    INITIALIZED = "INITIALIZED"
    INGESTING = "INGESTING"
    PARSING = "PARSING"
    ANALYZING_ARCHITECTURE = "ANALYZING_ARCHITECTURE"
    ANALYZING_SECURITY = "ANALYZING_SECURITY"
    RETRIEVING_RAG = "RETRIEVING_RAG"
    SYNTHESIZING = "SYNTHESIZING"
    ASSEMBLING = "ASSEMBLING"
    PERSISTING = "PERSISTING"
    COMPLETE = "COMPLETE"
    FAILED = "FAILED"


class NodeExecutionStatus(str, Enum):
    """Execution status of an individual LangGraph node recorded in the audit log."""

    RUNNING = "RUNNING"
    COMPLETE = "COMPLETE"
    FAILED = "FAILED"
    SKIPPED = "SKIPPED"


