class TestCoverageSignal(str, Enum):
    """Test file presence signal derived from the test-to-total-file ratio.

    Thresholds are defined in config/constants.py:
    - ABSENT:  ratio < TEST_FILE_PRESENCE_THRESHOLD_PARTIAL
    - PARTIAL: TEST_FILE_PRESENCE_THRESHOLD_PARTIAL <= ratio < TEST_FILE_PRESENCE_THRESHOLD_PRESENT
    - PRESENT: ratio >= TEST_FILE_PRESENCE_THRESHOLD_PRESENT
    """

    PRESENT = "PRESENT"
    PARTIAL = "PARTIAL"
    ABSENT = "ABSENT"


